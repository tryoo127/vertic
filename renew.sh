echo -e "========================================"
else
clear
echo -e ""
echo -e "======================================"
echo -e ""
echo -e "        Username Doesnt Exist        "
echo -e ""
echo -e "======================================"
fi
echo ""
read -n 1 -s -r -p "Press any key to back on menu"
menu